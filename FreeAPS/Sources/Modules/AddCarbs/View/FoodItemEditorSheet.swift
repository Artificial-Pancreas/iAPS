import Foundation
import SwiftUI

private let foodEditorFormatter: NumberFormatter = {
    let formatter = NumberFormatter()
    formatter.numberStyle = .decimal
    formatter.maximumFractionDigits = 2
    formatter.minimumFractionDigits = 0
    formatter.usesGroupingSeparator = false
    return formatter
}()

private func parseFoodEditorDecimal(_ text: String) -> Decimal? {
    guard !text.trimmingCharacters(in: .whitespaces).isEmpty else { return nil }
    return foodEditorFormatter.number(from: text)?.decimalValue
}

private func formatFoodEditorDecimal(_ value: Decimal) -> String {
    foodEditorFormatter.string(from: NSDecimalNumber(decimal: value)) ?? ""
}

// MARK: - FoodItemEditorSheet

struct FoodItemEditorSheet: View {
    let existingItem: FoodItemDetailed?
    let title: String
    let onSave: (FoodItemDetailed) -> Void
    let onCancel: () -> Void
    let allowServingMultiplierEdit: Bool
    let allExistingTags: Set<String>
    let showTagsAndFavorite: Bool

    @State private var nutritionMode: NutritionEntryMode = .perServing
    @State private var servingSizeUnit: MealUnits = .grams
    @State private var portionSizeOrMultiplier: Decimal = 1
    @State private var editedName: String = ""
    @State private var nutrientTexts: [NutrientType: String] = [:]
    @State private var servingSizeText: String = ""
    @State private var sliderMultiplier: Double = 1.0
    @State private var editedTags: Set<String> = []
    @State private var showingAddNewTag = false
    @State private var newTagText: String = ""

    @FocusState private var focusedField: NutritionField?

    enum NutritionField: Hashable {
        case nutrient(NutrientType)
        case servingSize
        case name
    }

    enum NutritionEntryMode: String, CaseIterable, Hashable {
        case perServing = "Per Serving"
        case per100g = "Per 100g"
        case per100ml = "Per 100ml"

        var nutritionType: NutritionEntryType {
            switch self {
            case .perServing: return .perServing
            case .per100g,
                 .per100ml: return .per100
            }
        }

        var unit: MealUnits {
            switch self {
            case .per100g,
                 .perServing: return .grams
            case .per100ml: return .milliliters
            }
        }
    }

    enum NutritionEntryType {
        case perServing
        case per100
    }

    private var nutritionType: NutritionEntryType {
        nutritionMode.nutritionType
    }

    private var selectedUnit: MealUnits {
        nutritionMode == .perServing ? servingSizeUnit : nutritionMode.unit
    }

    private var parsedNutrients: [NutrientType: Decimal] {
        nutrientTexts.compactMapValues { parseFoodEditorDecimal($0) }
    }

    private var canSave: Bool {
        NutrientType.allCases.filter { $0.isPrimary }.allSatisfy { (parsedNutrients[$0] ?? 0) >= 0 }
    }

    init(
        existingItem: FoodItemDetailed?,
        title: String? = nil,
        allowServingMultiplierEdit: Bool = false,
        allExistingTags: Set<String> = [],
        showTagsAndFavorite: Bool = false,
        onSave: @escaping (FoodItemDetailed) -> Void,
        onCancel: @escaping () -> Void
    ) {
        self.existingItem = existingItem
        self.title = title ?? (existingItem != nil ? "Edit Food" : "Add Food Manually")
        self.allowServingMultiplierEdit = allowServingMultiplierEdit
        self.allExistingTags = allExistingTags
        self.showTagsAndFavorite = showTagsAndFavorite
        self.onSave = onSave
        self.onCancel = onCancel

        if let item = existingItem {
            _editedName = State(initialValue: item.name)
            _editedTags = State(initialValue: Set(item.tags ?? []))

            switch item.nutrition {
            case let .per100(values):
                let mode: NutritionEntryMode = (item.units ?? .grams) == .milliliters ? .per100ml : .per100g
                _nutritionMode = State(initialValue: mode)
                _nutrientTexts = State(initialValue: values.mapValues { formatFoodEditorDecimal($0) })
                _portionSizeOrMultiplier = State(initialValue: item.portionSize ?? 100)
                _sliderMultiplier = State(initialValue: Double(item.portionSize ?? 100))

            case let .perServing(values):
                _nutritionMode = State(initialValue: .perServing)
                _servingSizeUnit = State(initialValue: item.units ?? .grams)
                _nutrientTexts = State(initialValue: values.mapValues { formatFoodEditorDecimal($0) })
                _portionSizeOrMultiplier = State(initialValue: item.servingsMultiplier ?? 1)
                _sliderMultiplier = State(initialValue: Double(item.servingsMultiplier ?? 1))
            }

            if let servingSize = item.standardServingSize, servingSize > 0 {
                _servingSizeText = State(initialValue: formatFoodEditorDecimal(servingSize))
            }
        }
    }

    private func autoGeneratedName() -> String {
        let carbs = parsedNutrients[.carbs] ?? 0
        let protein = parsedNutrients[.protein] ?? 0
        let fat = parsedNutrients[.fat] ?? 0

        let total = carbs + protein + fat
        guard total > 0 else { return "Food" }

        let carbPercent = (carbs / total) * 100
        let proteinPercent = (protein / total) * 100
        let fatPercent = (fat / total) * 100

        if carbPercent < 10 {
            if proteinPercent > 50 { return "Lean Protein" }
            if fatPercent > 60 { return "Fatty Food" }
            return "Low-Carb Food"
        }

        if carbPercent > 50 {
            if carbPercent > 80 { return "Starchy Food" }
            return "Carb Food"
        }

        if proteinPercent > 50 {
            if fatPercent < 10 { return "Lean Protein" }
            return "Protein Food"
        }

        if fatPercent > 50 {
            return "Fatty Food"
        }

        if carbPercent > 30 && proteinPercent > 30 {
            return "Mixed Food"
        }

        return "Food"
    }

    var body: some View {
        NavigationStack {
            VStack(spacing: 0) {
                // Nutrition Mode Picker at the top (3-way)
                VStack(spacing: 12) {
                    Picker("Nutrition Type", selection: $nutritionMode) {
                        ForEach(NutritionEntryMode.allCases, id: \.self) { mode in
                            Text(mode.rawValue).tag(mode)
                        }
                    }
                    .pickerStyle(.segmented)
                    .padding(.horizontal)
                    .padding(.top, 16)
                    .onChange(of: nutritionMode) { oldMode, newMode in
                        handleNutritionModeChange(from: oldMode, to: newMode)
                    }
                }

                FoodItemNutritionEditor(
                    nutritionMode: $nutritionMode,
                    servingSizeUnit: $servingSizeUnit,
                    portionSizeOrMultiplier: $portionSizeOrMultiplier,
                    sliderMultiplier: $sliderMultiplier,
                    nutrientTexts: $nutrientTexts,
                    servingSizeText: $servingSizeText,
                    allowServingMultiplierEdit: allowServingMultiplierEdit,
                    focusedField: $focusedField
                )

                // Editable food name with favorite tag
                VStack(alignment: .leading, spacing: 8) {
                    Text("Food Name")
                        .font(.caption)
                        .fontWeight(.semibold)
                        .foregroundColor(.secondary)
                        .padding(.horizontal)

                    HStack(spacing: 8) {
                        TextField(autoGeneratedName(), text: $editedName)
                            .font(.body)
                            .textFieldStyle(.roundedBorder)
                            .focused($focusedField, equals: .name)

                        if showTagsAndFavorite {
                            Button(action: {
                                withAnimation(.easeInOut(duration: 0.2)) {
                                    if editedTags.contains(FoodTags.favorites) {
                                        editedTags.remove(FoodTags.favorites)
                                    } else {
                                        editedTags.insert(FoodTags.favorites)
                                    }
                                }
                            }) {
                                Image(systemName: editedTags.contains(FoodTags.favorites) ? "star.fill" : "star")
                                    .font(.system(size: 14, weight: .medium))
                                    .foregroundColor(editedTags.contains(FoodTags.favorites) ? .white : .orange)
                                    .padding(.horizontal, 8)
                                    .padding(.vertical, 6)
                                    .background(
                                        RoundedRectangle(cornerRadius: 8, style: .continuous)
                                            .fill(
                                                editedTags.contains(FoodTags.favorites) ? Color.orange.opacity(0.75) : Color
                                                    .orange.opacity(0.08)
                                            )
                                    )
                                    .overlay(
                                        RoundedRectangle(cornerRadius: 8, style: .continuous)
                                            .strokeBorder(
                                                editedTags.contains(FoodTags.favorites) ? Color.clear : Color.orange
                                                    .opacity(0.35),
                                                lineWidth: 1.0
                                            )
                                    )
                            }
                            .buttonStyle(.plain)
                        }
                    }
                    .padding(.horizontal)
                }
                .padding(.top, 12)
                .padding(.bottom, 8)

                // Collapsible tags section (only show if showTagsAndFavorite is true)
                if showTagsAndFavorite {
                    CollapsibleTagsSection(
                        selectedTags: $editedTags,
                        allExistingTags: allExistingTags,
                        showingAddNewTag: $showingAddNewTag
                    )
                    .padding(.horizontal)
                    .padding(.bottom, 8)
                }

                // Action buttons at bottom
                HStack(spacing: 12) {
                    Button("Cancel") {
                        onCancel()
                    }
                    .frame(maxWidth: .infinity)
                    .padding(.vertical, 12)
                    .background(Color.gray.opacity(0.2))
                    .foregroundColor(.primary)
                    .cornerRadius(10)

                    Button("Save") {
                        saveFoodItem()
                    }
                    .frame(maxWidth: .infinity)
                    .padding(.vertical, 12)
                    .background(canSave ? Color.accentColor : Color.gray.opacity(0.2))
                    .foregroundColor(canSave ? .white : .secondary)
                    .cornerRadius(10)
                    .disabled(!canSave)
                }
                .padding(.horizontal)
                .padding(.vertical, 16)
                .background(Color(.systemBackground))
            }
            .navigationTitle(title)
            .navigationBarTitleDisplayMode(.inline)
            .toolbar {
                if focusedField != nil {
                    ToolbarItemGroup(placement: .keyboard) {
                        Spacer()
                        Button {
                            focusedField = nil
                        } label: {
                            Image(systemName: "keyboard.chevron.compact.down")
                                .font(.body.weight(.semibold))
                                .foregroundStyle(.blue)
                        }
                    }
                }
            }
        }
        .onDisappear {
            focusedField = nil
        }
        .alert("Add New Tag", isPresented: $showingAddNewTag) {
            TextField("Tag name", text: $newTagText)
                .autocapitalization(.none)
            Button("Cancel", role: .cancel) {
                newTagText = ""
            }
            Button("Add") {
                let trimmed = newTagText.trimmingCharacters(in: .whitespacesAndNewlines).lowercased()
                if !trimmed.isEmpty, trimmed != FoodTags.favorites {
                    editedTags.insert(trimmed)
                }
                newTagText = ""
            }
        } message: {
            Text("Enter a tag name (e.g., breakfast, snack, low-carb)")
        }
    }

    private func handleNutritionModeChange(from oldMode: NutritionEntryMode, to newMode: NutritionEntryMode) {
        guard oldMode.nutritionType != newMode.nutritionType else { return }
        let nutrients = parsedNutrients

        switch (oldMode.nutritionType, newMode.nutritionType) {
        case (.per100, .perServing):
            let scaleFactor = portionSizeOrMultiplier / 100
            for nutrient in NutrientType.allCases {
                if let value = nutrients[nutrient] {
                    nutrientTexts[nutrient] = formatFoodEditorDecimal(round(value * scaleFactor, to: 1))
                }
            }
            servingSizeText = formatFoodEditorDecimal(portionSizeOrMultiplier)
            portionSizeOrMultiplier = 1
            sliderMultiplier = 1.0

        case (.perServing, .per100):
            if let servingSize = parseFoodEditorDecimal(servingSizeText), servingSize > 0 {
                let scaleFactor = 100 / servingSize
                for nutrient in NutrientType.allCases {
                    if let value = nutrients[nutrient] {
                        nutrientTexts[nutrient] = formatFoodEditorDecimal(round(value * scaleFactor, to: 1))
                    }
                }
                portionSizeOrMultiplier = servingSize
                sliderMultiplier = Double(servingSize)
            } else {
                portionSizeOrMultiplier = 100
                sliderMultiplier = 100.0
            }

        default:
            break
        }
    }

    private func round(_ value: Decimal, to places: Int) -> Decimal {
        var rounded = value
        var result = Decimal()
        NSDecimalRound(&result, &rounded, places, .plain)
        return result
    }

    private func saveFoodItem() {
        let finalName = editedName.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty ?
            autoGeneratedName() : editedName

        var nutritionValues: [NutrientType: Decimal] = [:]
        for nutrient in NutrientType.allCases {
            if let value = parseFoodEditorDecimal(nutrientTexts[nutrient] ?? "") {
                nutritionValues[nutrient] = value
            } else if nutrient.isPrimary {
                nutritionValues[nutrient] = 0
            }
        }

        let standardServingSize: Decimal? = {
            guard let value = parseFoodEditorDecimal(servingSizeText), value > 0 else { return nil }
            return value
        }()

        let tagsArray: [String]? = {
            guard !editedTags.isEmpty else { return nil }
            var result = Array(editedTags)
            result.sort { tag1, tag2 in
                if tag1 == FoodTags.favorites { return true }
                if tag2 == FoodTags.favorites { return false }
                return tag1 < tag2
            }
            return result
        }()

        let foodItem: FoodItemDetailed

        if let existingItem = existingItem {
            switch nutritionMode.nutritionType {
            case .per100:
                foodItem = FoodItemDetailed(
                    id: existingItem.id,
                    name: finalName,
                    nutritionPer100: nutritionValues,
                    portionSize: portionSizeOrMultiplier,
                    confidence: existingItem.confidence,
                    brand: existingItem.brand,
                    standardServing: existingItem.standardServing,
                    standardServingSize: standardServingSize,
                    units: selectedUnit,
                    preparationMethod: existingItem.preparationMethod,
                    visualCues: existingItem.visualCues,
                    glycemicIndex: existingItem.glycemicIndex,
                    assessmentNotes: existingItem.assessmentNotes,
                    imageURL: existingItem.imageURL,
                    tags: tagsArray,
                    source: existingItem.source
                )
            case .perServing:
                foodItem = FoodItemDetailed(
                    id: existingItem.id,
                    name: finalName,
                    nutritionPerServing: nutritionValues,
                    servingsMultiplier: portionSizeOrMultiplier,
                    confidence: existingItem.confidence,
                    brand: existingItem.brand,
                    standardServing: existingItem.standardServing,
                    standardServingSize: standardServingSize,
                    units: selectedUnit,
                    preparationMethod: existingItem.preparationMethod,
                    visualCues: existingItem.visualCues,
                    glycemicIndex: existingItem.glycemicIndex,
                    assessmentNotes: existingItem.assessmentNotes,
                    imageURL: existingItem.imageURL,
                    tags: tagsArray,
                    source: existingItem.source
                )
            }
        } else {
            switch nutritionMode.nutritionType {
            case .per100:
                foodItem = FoodItemDetailed(
                    name: finalName,
                    nutritionPer100: nutritionValues,
                    portionSize: portionSizeOrMultiplier,
                    confidence: nil,
                    brand: nil,
                    standardServing: nil,
                    standardServingSize: standardServingSize,
                    units: selectedUnit,
                    preparationMethod: nil,
                    visualCues: nil,
                    glycemicIndex: nil,
                    assessmentNotes: nil,
                    imageURL: nil,
                    tags: tagsArray,
                    source: .manual
                )
            case .perServing:
                foodItem = FoodItemDetailed(
                    name: finalName,
                    nutritionPerServing: nutritionValues,
                    servingsMultiplier: portionSizeOrMultiplier,
                    confidence: nil,
                    brand: nil,
                    standardServing: nil,
                    standardServingSize: standardServingSize,
                    units: selectedUnit,
                    preparationMethod: nil,
                    visualCues: nil,
                    glycemicIndex: nil,
                    assessmentNotes: nil,
                    imageURL: nil,
                    tags: tagsArray,
                    source: .manual
                )
            }
        }

        onSave(foodItem)
    }
}

private struct FoodItemNutritionEditor: View {
    @Binding var nutritionMode: FoodItemEditorSheet.NutritionEntryMode
    @Binding var servingSizeUnit: MealUnits
    @Binding var portionSizeOrMultiplier: Decimal
    @Binding var sliderMultiplier: Double
    @Binding var nutrientTexts: [NutrientType: String]
    @Binding var servingSizeText: String
    let allowServingMultiplierEdit: Bool
    @FocusState.Binding var focusedField: FoodItemEditorSheet.NutritionField?

    @State private var showAllNutrients: Bool = false

    init(
        nutritionMode: Binding<FoodItemEditorSheet.NutritionEntryMode>,
        servingSizeUnit: Binding<MealUnits>,
        portionSizeOrMultiplier: Binding<Decimal>,
        sliderMultiplier: Binding<Double>,
        nutrientTexts: Binding<[NutrientType: String]>,
        servingSizeText: Binding<String>,
        allowServingMultiplierEdit: Bool = false,
        focusedField: FocusState<FoodItemEditorSheet.NutritionField?>.Binding
    ) {
        _nutritionMode = nutritionMode
        _servingSizeUnit = servingSizeUnit
        _portionSizeOrMultiplier = portionSizeOrMultiplier
        _sliderMultiplier = sliderMultiplier
        _nutrientTexts = nutrientTexts
        _servingSizeText = servingSizeText
        self.allowServingMultiplierEdit = allowServingMultiplierEdit
        _focusedField = focusedField

        let hasOptionalNutrients = NutrientType.allCases.contains {
            !$0.isPrimary && (parseFoodEditorDecimal(nutrientTexts.wrappedValue[$0] ?? "") ?? 0) > 0
        } || (parseFoodEditorDecimal(servingSizeText.wrappedValue) ?? 0) > 0
        _showAllNutrients = State(initialValue: hasOptionalNutrients)
    }

    private var unit: String {
        nutritionMode == .perServing ? "serving" : nutritionMode.unit.dimension.symbol
    }

    private var nutritionType: FoodItemEditorSheet.NutritionEntryType {
        nutritionMode.nutritionType
    }

    private var sliderRange: ClosedRange<Double> {
        switch nutritionType {
        case .per100: return 10.0 ... 600.0
        case .perServing: return 0.25 ... 10.0
        }
    }

    private var sliderStep: Double.Stride {
        switch nutritionType {
        case .per100: return 5.0
        case .perServing: return 0.25
        }
    }

    private var sliderMinLabel: String {
        switch nutritionType {
        case .per100: return "10 \(nutritionMode.unit.dimension.symbol)"
        case .perServing: return "0.25×"
        }
    }

    private var sliderMaxLabel: String {
        switch nutritionType {
        case .per100: return "600 \(nutritionMode.unit.dimension.symbol)"
        case .perServing: return "10×"
        }
    }

    var body: some View {
        ScrollView {
            VStack(alignment: .leading, spacing: 16) {
                // Portion Size/Multiplier Slider
                if nutritionType == .per100 || (nutritionType == .perServing && allowServingMultiplierEdit) {
                    VStack(spacing: 12) {
                        switch nutritionType {
                        case .per100:
                            Text(
                                "\(Double(portionSizeOrMultiplier), specifier: "%.0f") \(nutritionMode.unit.dimension.symbol)"
                            )
                            .font(.system(size: 28, weight: .bold))
                            .foregroundColor(.orange)
                        case .perServing:
                            Text("\(Double(portionSizeOrMultiplier), specifier: "%.2f")× servings")
                                .font(.system(size: 28, weight: .bold))
                                .foregroundColor(.orange)
                        }

                        Slider(value: $sliderMultiplier, in: sliderRange, step: sliderStep)
                            .tint(.orange)
                            .padding(.horizontal)
                            .onChange(of: sliderMultiplier) { _, newValue in
                                portionSizeOrMultiplier = Decimal(newValue)
                            }

                        HStack {
                            Text(sliderMinLabel)
                                .font(.caption)
                                .foregroundColor(.secondary)
                            Spacer()
                            Text(sliderMaxLabel)
                                .font(.caption)
                                .foregroundColor(.secondary)
                        }
                        .padding(.horizontal)
                    }
                    .padding(.vertical, 8)
                }

                // Nutrition Table
                VStack(spacing: 8) {
                    // Header row
                    HStack(spacing: 8) {
                        Text("")
                            .frame(maxWidth: .infinity, alignment: .leading)

                        Text("This portion")
                            .font(.caption)
                            .fontWeight(.semibold)
                            .foregroundColor(.secondary)
                            .frame(width: 95, alignment: .trailing)

                        Text(nutritionType == .perServing ? "Per serving" : "Per 100\(nutritionMode.unit.dimension.symbol)")
                            .font(.caption)
                            .fontWeight(.semibold)
                            .foregroundColor(.secondary)
                            .frame(width: 100, alignment: .trailing)
                    }
                    .padding(.horizontal, 12)
                    .padding(.top, 8)

                    Divider()

                    // Primary nutrients (always visible)
                    ForEach(NutrientType.allCases.filter { $0.isPrimary }) { nutrient in
                        let baseValue = parseFoodEditorDecimal(nutrientTexts[nutrient] ?? "") ?? 0
                        FoodItemNutritionRow(
                            localizedLabel: nutrient.localizedLabel,
                            portionValue: calculatePortionValue(baseValue: baseValue),
                            text: Binding(
                                get: { nutrientTexts[nutrient] ?? "" },
                                set: { nutrientTexts[nutrient] = $0 }
                            ),
                            unit: nutrient.unit,
                            focusedField: $focusedField,
                            fieldTag: .nutrient(nutrient)
                        )
                        Divider()
                    }

                    // Calculated calories (always visible, read-only)
                    let parsedForCalories = nutrientTexts.compactMapValues { parseFoodEditorDecimal($0) }
                    FoodItemCaloriesDisplayRow(
                        portionCalories: calculatePortionValue(baseValue: parsedForCalories.calories),
                        baseCalories: parsedForCalories.calories
                    )

                    // Secondary nutrients (shown when expanded)
                    if showAllNutrients {
                        ForEach(NutrientType.allCases.filter { !$0.isPrimary }) { nutrient in
                            Divider()
                            let baseValue = parseFoodEditorDecimal(nutrientTexts[nutrient] ?? "") ?? 0
                            FoodItemNutritionRow(
                                localizedLabel: nutrient.localizedLabel,
                                portionValue: calculatePortionValue(baseValue: baseValue),
                                text: Binding(
                                    get: { nutrientTexts[nutrient] ?? "" },
                                    set: { nutrientTexts[nutrient] = $0 }
                                ),
                                unit: nutrient.unit,
                                focusedField: $focusedField,
                                fieldTag: .nutrient(nutrient)
                            )
                        }

                        Divider()
                        FoodItemServingSizeRow(
                            text: $servingSizeText,
                            unit: nutritionMode == .perServing ? $servingSizeUnit : .constant(nutritionMode.unit),
                            allowToggle: nutritionMode == .perServing,
                            focusedField: $focusedField,
                            fieldTag: .servingSize
                        )
                    }

                    // Button to reveal optional nutrients
                    if !showAllNutrients {
                        Button(action: {
                            withAnimation(.easeInOut(duration: 0.2)) {
                                showAllNutrients = true
                            }
                        }) {
                            HStack {
                                Image(systemName: "chevron.down")
                                    .font(.caption)
                                    .foregroundColor(.secondary)
                                Text("Show All Nutrients")
                                    .font(.caption)
                                    .foregroundColor(.secondary)
                            }
                            .padding(.vertical, 8)
                            .frame(maxWidth: .infinity)
                        }
                        .buttonStyle(PlainButtonStyle())
                    }
                }
                .padding(12)
                .background(
                    RoundedRectangle(cornerRadius: 12, style: .continuous)
                        .fill(Color(.secondarySystemBackground))
                )
                .padding(.horizontal)

                Spacer(minLength: 8)
            }
            .padding(.vertical)
        }
        .scrollDismissesKeyboard(.immediately)
    }

    private func calculatePortionValue(baseValue: Decimal) -> Decimal {
        switch nutritionType {
        case .per100:
            return baseValue / 100 * portionSizeOrMultiplier
        case .perServing:
            return baseValue * portionSizeOrMultiplier
        }
    }
}

private struct FoodItemNutritionRow: View {
    let localizedLabel: String
    let portionValue: Decimal
    @Binding var text: String
    let unit: Dimension
    @FocusState.Binding var focusedField: FoodItemEditorSheet.NutritionField?
    let fieldTag: FoodItemEditorSheet.NutritionField

    var body: some View {
        HStack(spacing: 8) {
            Text(localizedLabel)
                .font(.subheadline)
                .foregroundColor(.primary.opacity(0.8))
                .frame(maxWidth: .infinity, alignment: .leading)

            // Per portion value (calculated, read-only)
            HStack(spacing: 2) {
                Text("\(Double(portionValue), specifier: "%.1f")")
                    .font(.subheadline)
                    .fontWeight(.medium)
                    .foregroundColor(.primary)
                Text(unit.symbol)
                    .font(.caption2)
                    .foregroundColor(.secondary.opacity(0.7))
                    .frame(width: 28, alignment: .leading)
            }
            .frame(width: 95, alignment: .trailing)

            HStack(spacing: 4) {
                TextField("0", text: $text)
                    .font(.subheadline)
                    .foregroundColor(.secondary)
                    .textFieldStyle(.roundedBorder)
                    .keyboardType(.decimalPad)
                    .multilineTextAlignment(.trailing)
                    .focused($focusedField, equals: fieldTag)
                    .cursorAtEndOnFocus()

                Text(unit.symbol)
                    .font(.caption2)
                    .foregroundColor(.secondary.opacity(0.7))
                    .frame(width: 28, alignment: .leading)
            }
            .frame(width: 100, alignment: .trailing)
        }
        .padding(.leading, 12)
        .padding(.trailing, 6)
        .padding(.vertical, 6)
    }
}

private struct FoodItemCaloriesDisplayRow: View {
    let portionCalories: Decimal
    let baseCalories: Decimal

    var body: some View {
        HStack(spacing: 8) {
            HStack(spacing: 4) {
                Text(NSLocalizedString("Calories", comment: ""))
                    .font(.subheadline)
                    .foregroundColor(.primary.opacity(0.8))
                Image(systemName: "function")
                    .font(.system(size: 11))
                    .foregroundColor(.blue.opacity(0.8))
            }
            .frame(maxWidth: .infinity, alignment: .leading)

            HStack(spacing: 2) {
                Text("\(Int(truncating: portionCalories as NSNumber))")
                    .font(.subheadline)
                    .fontWeight(.medium)
                    .foregroundColor(.primary)
                Text(UnitEnergy.kilocalories.symbol)
                    .font(.caption2)
                    .foregroundColor(.secondary.opacity(0.7))
                    .frame(width: 28, alignment: .leading)
            }
            .frame(width: 95, alignment: .trailing)

            HStack(spacing: 2) {
